#!/usr/bin/env python3
"""
0-add module
Provides a type-annotated function to add two float numbers.
"""

def add(a: float, b: float) -> float:
    """
    Add two float numbers.

    Args:
        a (float): The first number.
        b (float): The second number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
